public class originalascii {

    public static void main(String[] args) {
// An ASCII art is made from lines of ASCII characters that resemble a drawing/photo when put together
// correctly. The code below prints a small bird (double backslash prints one backslash).
        System.out.println("   \\\\");
        System.out.println("   (o>");
        System.out.println("\\_//)");
        System.out.println(" \\_/_)");
        System.out.println("  _|_");
    }
    
}
